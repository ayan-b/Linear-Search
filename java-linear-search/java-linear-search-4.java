import java.util.*;
import java.lang.*;
import java.io.*;

class LinearSearch
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int size=sc.nextInt();
		int a[]=new int[size];
		for (int i=0;i<size;i++)
		{
		    a[i]=sc.nextInt();
		}
		int num=sc.nextInt();
		for (int i=0;i<size;i++)
		{
		    if(a[i]==num)
		    {
		        System.out.println("Number is at position: "+ (i+1));
		        break;
		    }
		}
	}
}
